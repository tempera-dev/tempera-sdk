#!/usr/bin/env python3
"""Generate the Mintlify documentation site (docs/site/) from surface.json.

surface.json is the single source of truth for the Tempera SDK: products,
audiences, scopes, environment targets, the error contract, every typed
operation, and the MCP gateway. This script renders it (plus docs/ROLLOUT.md
for the rollout page) into a complete Mintlify project:

  docs/site/docs.json                    Mintlify config + navigation
  docs/site/index.mdx                    overview, quickstart, product table
  docs/site/authentication.mdx           OAuth2 + PKCE, tp_ keys, audiences, scopes
  docs/site/environments.mdx             environment presets + base-URL precedence
  docs/site/errors.mdx                   TemperaApiError, wire shapes, MCP errors
  docs/site/mcp-gateway.mdx              unified MCP gateway
  docs/site/rollout.mdx                  rendered from docs/ROLLOUT.md
  docs/site/products/<product>.mdx       one page per typed product, every operation
  docs/site/products/other-products.mdx  private registry-only contract inventory

The generated site is committed; scripts/check-sdk-surface.py re-renders it
and fails on any diff, so the docs cannot drift from the manifest. Rendering
is deterministic: sorted iteration for derived aggregates, no timestamps.

Usage:
  python3 scripts/gen-sdk-docs.py           # write docs/site/ (removes strays)
  python3 scripts/gen-sdk-docs.py --check   # exit 1 if docs/site/ is stale
"""
from __future__ import annotations

import json
import re
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
SURFACE = ROOT / "surface.json"
ROLLOUT = ROOT / "docs" / "ROLLOUT.md"
SITE = ROOT / "docs" / "site"

GENERATED_NOTE = (
    "{/* Generated by scripts/gen-sdk-docs.py from surface.json -- do not hand-edit. */}"
)

OVERVIEW_PAGES = ["index", "authentication", "environments", "errors", "mcp-gateway", "rollout"]

# The public product story is intentionally narrower than the versioned SDK
# registry. Keep this order aligned with the browser-agent quality loop.
PRIMARY_PRODUCT_KEYS = ("controlPlane", "tempo", "humanData", "palette")

# Meanings for the MCP gateway JSON-RPC error codes (keyed by surface.json name).
MCP_ERROR_MEANINGS = {
    "planLimit": "Plan limit exceeded (`plan_limit_exceeded`): the workspace's metered "
    "`mcp_invocations` allowance is exhausted; upgrade the plan or wait for the meter to reset.",
    "invalidRequest": "The payload is not a valid JSON-RPC 2.0 request.",
    "methodNotFound": "Unknown JSON-RPC method (or unknown tool name on `tools/call`).",
    "invalidParams": "The method was called with invalid or missing parameters.",
    "internalError": "The gateway or an upstream product MCP server failed.",
}


def snake(camel: str) -> str:
    """lowerCamelCase -> snake_case (matches gen-sdk-surface.py and the Rust keys)."""
    return re.sub(r"(?<!^)(?=[A-Z])", "_", camel).lower()


def snake_attr(camel: str) -> str:
    """lowerCamelCase -> snake_case as the Python client's attribute names do
    (consecutive capitals stay one word: tempOS -> temp_os)."""
    return re.sub(r"([a-z0-9])([A-Z]+)", r"\1_\2", camel).lower()


def kebab(camel: str) -> str:
    return re.sub(r"(?<!^)(?=[A-Z])", "-", camel).lower()


def esc(text: str) -> str:
    """Escape prose for MDX: literal braces and '<' would otherwise start JSX."""
    return (
        text.replace("\\", "\\\\")
        .replace("{", "\\{")
        .replace("}", "\\}")
        .replace("<", "\\<")
    )


def cell(text: str) -> str:
    """Escape prose for a Markdown table cell."""
    return esc(text).replace("|", "\\|")


def frontmatter(title: str, description: str) -> str:
    return "\n".join(
        [
            "---",
            f"title: {json.dumps(title)}",
            f"description: {json.dumps(description)}",
            "---",
            "",
            GENERATED_NOTE,
            "",
        ]
    )


def private_access_note() -> list[str]:
    return [
        "> **Private access.** Hosted Tempera services are available through design-partner onboarding.",
        "> Use the issuer, credentials, environment, and product URLs provisioned for your workspace;",
        "> start in staging unless Tempera explicitly approves another target.",
        "",
    ]


def code_group(blocks: list[tuple[str, str, str]]) -> list[str]:
    """A Mintlify <CodeGroup> from (language, tab title, code) blocks."""
    lines = ["<CodeGroup>", ""]
    for language, title, code in blocks:
        lines.append(f"```{language} {title}")
        lines.append(code.rstrip("\n"))
        lines.append("```")
        lines.append("")
    lines.append("</CodeGroup>")
    lines.append("")
    return lines


def typed_products(surface: dict) -> list[str]:
    return [key for key in surface["products"] if key in surface["operations"]]


def registry_products(surface: dict) -> list[str]:
    return [key for key in surface["products"] if key not in surface["operations"]]


def product_page_slug(product_key: str) -> str:
    return f"products/{kebab(product_key)}"


def auth_label(surface: dict, product_key: str, op: dict) -> str:
    kind = op["auth"]
    if kind == "none":
        return "None (public endpoint)."
    if kind == "account":
        return "Account bearer — the control-plane session token stored by `login`/`signup`."
    if kind == "introspectionSecret":
        return "Introspection secret bearer (server-side only)."
    audience = surface["products"][product_key]["audience"] or surface["defaultAudience"]
    return f"Product bearer — an access token minted for audience `{audience}`, or a central `tp_` API key."


def example_param_names(op: dict) -> list[str]:
    """Parameters shown in the per-operation example: every path parameter,
    plus a readable sample of the declared query/body parameters."""
    names = list(op.get("pathParams", []))
    if op["method"] in ("GET", "DELETE"):
        names += op.get("query", [])[:2]
    else:
        names += op.get("body", [])[:3]
    return names


def operation_example(surface: dict, product_key: str, op: dict) -> list[str]:
    names = example_param_names(op)
    ts_attr = product_key
    py_attr = snake_attr(product_key)
    rust_product = snake(product_key)
    op_snake = snake(op["id"])

    if names:
        ts_args = "{\n" + "".join(f'  {name}: "<{name}>",\n' for name in names) + "}"
        ts_code = f"const result = await client.{ts_attr}.{op['id']}({ts_args});"
        py_args = "{\n" + "".join(f'    "{name}": "<{name}>",\n' for name in names) + "}"
        py_code = f"result = client.{py_attr}.{op_snake}({py_args})"
        rust_args = "&[\n" + "".join(f'    ("{name}", "<{name}>".into()),\n' for name in names) + "]"
    else:
        ts_code = f"const result = await client.{ts_attr}.{op['id']}();"
        py_code = f"result = client.{py_attr}.{op_snake}()"
        rust_args = "&[]"
    rust_code = (
        f'let spec = client.build_request("{rust_product}", "{op_snake}", {rust_args})?;\n'
        "// Send spec.method / spec.full_url() / spec.headers / spec.body_json\n"
        "// with your own HTTP client."
    )
    return code_group(
        [
            ("typescript", "TypeScript", ts_code),
            ("python", "Python", py_code),
            ("rust", "Rust", rust_code),
        ]
    )


def param_table(title: str, rows: list[tuple[str, str, str]]) -> list[str]:
    lines = [f"**{title}**", "", "| Parameter | Required | Notes |", "|---|---|---|"]
    for name, required, notes in rows:
        lines.append(f"| `{name}` | {required} | {notes or '—'} |")
    lines.append("")
    return lines


def operation_section(surface: dict, product_key: str, op: dict) -> list[str]:
    op_snake = snake(op["id"])
    rust_product = snake(product_key)
    lines = [f"### {op['id']}", ""]
    lines.append(f"`{op['method']} {op['path']}`")
    lines.append("")
    lines.append(esc(op["description"]))
    lines.append("")
    lines.append(f"- **Auth:** {auth_label(surface, product_key, op)}")
    if op.get("scope"):
        lines.append(f"- **Scope:** `{op['scope']}`")
    lines.append(
        f"- **Call as:** TypeScript `client.{product_key}.{op['id']}()` · "
        f"Python `client.{snake_attr(product_key)}.{op_snake}()` · "
        f'Rust `build_request("{rust_product}", "{op_snake}", params)`'
    )
    lines.append("")

    defaults = op.get("bodyDefaults", {})
    if op.get("pathParams"):
        lines += param_table(
            "Path parameters",
            [(name, "yes", "Substituted into the URL path (percent-encoded).") for name in op["pathParams"]],
        )
    if op.get("query"):
        lines += param_table("Query parameters", [(name, "no", "") for name in op["query"]])
    body_rows: list[tuple[str, str, str]] = []
    required_body = set(op.get("requiredBody", []))
    for name in op.get("body", []):
        note = ""
        if name in defaults:
            note = f"Defaults to `{json.dumps(defaults[name])}` when omitted."
        body_rows.append((name, "yes" if name in required_body else "no", note))
    for name in sorted(set(defaults) - set(op.get("body", []))):
        body_rows.append(
            (name, "always sent", f"Included automatically as `{json.dumps(defaults[name])}`; pass `{name}` to override.")
        )
    if body_rows:
        lines += param_table("Body parameters", body_rows)
    lines += operation_example(surface, product_key, op)
    return lines


def render_docs_json(surface: dict) -> str:
    config = {
        "$schema": "https://mintlify.com/docs.json",
        "theme": "mint",
        "name": "Tempera SDK",
        "colors": {"primary": "#0D9373", "light": "#07C983", "dark": "#0D9373"},
        "navigation": {
            "groups": [
                {"group": "Overview", "pages": OVERVIEW_PAGES},
                {
                    "group": "API Reference",
                    "pages": [product_page_slug(key) for key in typed_products(surface)],
                },
                {"group": "Contract inventory", "pages": ["products/other-products"]},
            ]
        },
        "footer": {
            "socials": {"github": "https://github.com/tempera-dev/tempera-sdk"}
        },
    }
    return json.dumps(config, indent=2) + "\n"


