def validate_site(surface: dict, files: dict[str, str]) -> None:
    """Sanity checks: docs.json parses, every navigation page has a file, and
    every operation in the manifest appears on its product page."""
    config = json.loads(files["docs.json"])
    nav_pages = [
        page for group in config["navigation"]["groups"] for page in group["pages"]
    ]
    for page in nav_pages:
        assert f"{page}.mdx" in files, f"navigation page {page!r} has no generated file"
    mdx_pages = {path[:-4] for path in files if path.endswith(".mdx")}
    assert sorted(nav_pages) == sorted(mdx_pages), (
        f"navigation and generated pages differ: {sorted(set(nav_pages) ^ mdx_pages)}"
    )
    for product_key, ops in surface["operations"].items():
        page = files[f"{product_page_slug(product_key)}.mdx"]
        for op in ops:
            assert f"### {op['id']}\n" in page, (
                f"operation {product_key}.{op['id']} missing from its product page"
            )
        if product_key not in PRIMARY_PRODUCT_KEYS:
            assert "> **Contract inventory.**" in page, (
                f"{product_key}: non-primary reference is missing its contract-inventory notice"
            )
    registry_page = files["products/other-products.mdx"]
    for raw_route_marker in ('.request("/', ".request('/"):
        assert raw_route_marker not in registry_page, (
            "registry-only docs must not invent a copyable raw route without a tested contract"
        )
    assert "does not by itself prove a Palette trace exists" in files["index.mdx"], (
        "overview must keep the Tempo-session/Palette-evidence boundary explicit"
    )
    forbidden_public_examples = [
        'issuerUrl: "https://api.tempera.dev"',
        'issuer_url="https://api.tempera.dev"',
        'TemperaAuth::new("https://api.tempera.dev")',
        'environment: "production"',
        'environment="production"',
    ]
    for path, content in files.items():
        if path == "environments.mdx":
            continue  # The reserved production target remains part of the reference contract.
        for forbidden in forbidden_public_examples:
            assert forbidden not in content, (
                f"{path}: public example presents reserved production access: {forbidden!r}"
            )
    for path, content in files.items():
        if path.endswith(".mdx"):
            assert GENERATED_NOTE in content, f"{path}: missing generated-by note"


def existing_site_files() -> list[Path]:
    if not SITE.exists():
        return []
    return sorted(path for path in SITE.rglob("*") if path.is_file())


def main() -> int:
    surface = json.loads(SURFACE.read_text())
    files = render_site(surface, ROLLOUT.read_text())
    check = "--check" in sys.argv

    if check:
        stale: list[str] = []
        for rel_path, rendered in sorted(files.items()):
            target = SITE / rel_path
            if not target.exists() or target.read_text() != rendered:
                stale.append(rel_path)
        expected = {SITE / rel_path for rel_path in files}
        for path in existing_site_files():
            if path not in expected:
                stale.append(f"{path.relative_to(SITE)} (stray file not produced by the generator)")
        if stale:
            for rel_path in stale:
                print(
                    f"stale generated docs: docs/site/{rel_path} (run python3 scripts/gen-sdk-docs.py)",
                    file=sys.stderr,
                )
            return 1
        print(f"generated docs site is current ({len(files)} files)")
        return 0

    expected = {SITE / rel_path for rel_path in files}
    for path in existing_site_files():
        if path not in expected:
            path.unlink()
            print(f"removed stray docs/site/{path.relative_to(SITE)}")
    for rel_path, rendered in sorted(files.items()):
        target = SITE / rel_path
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(rendered)
        print(f"wrote docs/site/{rel_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
